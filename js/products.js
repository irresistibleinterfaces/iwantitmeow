window.findProductsByCategory = (category) =>
  (window.PRODUCTS || []).filter(p => p.categories.includes(category));


// ---------------------------
// HOMEPAGE CAROUSELS
// ---------------------------
document.addEventListener("DOMContentLoaded", () => {
  const categoriesToShow = ["pop-culture", "cosplay-classics", "accessories"];
  const container = document.getElementById("category-carousels");
  if (!container) return;

  categoriesToShow.forEach(cat => {
    const catData = window.CATEGORIES?.[cat];
    const products = window.findProductsByCategory(cat).slice(0, 6);
    if (!products.length) return;

    const slides = products.map(p => `
      <div class="swiper-slide">
        <div class="product-item" data-id="${p.id}">
          <div class="image-holder">
            <a href="product.html?id=${p.id}">
              <img src="${p.img}" alt="${p.name}" class="product-image">
            </a>
          </div>
          <div class="product-detail">
            <h3 class="product-title">
              <a href="product.html?id=${p.id}">${p.name}</a>
            </h3>
            <span class="item-price text-primary">A$${p.price.toFixed(2)}</span>
          </div>
        </div>
      </div>
    `).join("");

    container.insertAdjacentHTML("beforeend", `
      <div class="category-block mb-5">
        <div class="section-header d-flex flex-wrap align-items-center justify-content-between">
          <h3 class="section-title">${catData?.name || cat}</h3>
          <div class="btn-wrap">
            <a href="${cat}.html" class="d-flex align-items-center">
              View all <i class="icon icon-arrow-io"></i>
            </a>
          </div>
        </div>
        <div class="swiper category-swiper-${cat} overflow-hidden">
          <div class="swiper-wrapper">${slides}</div>
          <div class="swiper-pagination"></div>
        </div>
      </div>
    `);

    new Swiper(`.category-swiper-${cat}`, {
      slidesPerView: 4,
      spaceBetween: 30,
      loop: true,
      pagination: {
        el: `.category-swiper-${cat} .swiper-pagination`,
        clickable: true
      },
      breakpoints: {
        320: { slidesPerView: 1 },
        768: { slidesPerView: 2 },
        992: { slidesPerView: 3 },
        1200: { slidesPerView: 4 },
      }
    });
  });
});
